#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	answ;
	int	sign;

	sign = 1;
	i = 0;
	answ = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		sign *= -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		answ = answ * 10 + (str[i] - '0');
		i++;
	}
	return (answ * sign);
}
